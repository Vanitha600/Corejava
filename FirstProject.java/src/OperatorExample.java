
public class OperatorExample {

	public static void main(String[] args) {
			int numberOfDaysCount = 10;
			
			
			System.out.println("numberOfDaysCount" +numberOfDaysCount);
			numberOfDaysCount++;
			
			System.out.println("numberOfDaysCount" +numberOfDaysCount);
			numberOfDaysCount = numberOfDaysCount+2;
			
			System.out.println("numberOfDaysCount" +numberOfDaysCount);
			
			
	}

}
